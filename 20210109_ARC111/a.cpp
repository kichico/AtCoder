#include <bits/stdc++.h>
using namespace std;

long long pow(long long x, long long n) {
    long long ret = 1;
    while (n > 0) {
        if (n & 1) ret *= x;  // n の最下位bitが 1 ならば x^(2^i) をかける
        x *= x;
        n >>= 1;  // n を1bit 左にずらす
    }
    return ret;
}

long long modinv(long long a, long long m) {
    long long b = m, u = 1, v = 0;
    while (b) {
        long long t = a / b;
        a -= t * b; swap(a, b);
        u -= t * v; swap(u, v);
    }
    u %= m;
    if (u < 0) u += m;
    return u;
}

int main() {
    long long N,M;
    cin >> N >> M;
    N=pow(10, N);
    N%=M;
    long long ans=N*modinv(M,N);
    cout<<(long long)floor(ans)%M<<endl;
    return 0;
}