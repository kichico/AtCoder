int create_int_rand(std::mt19937 mt, int xmin, int xmax) {
    std::uniform_int_distribution<> rd(xmin, xmax);
    return rd(mt);
}

int random(int N) {
    std::random_device rnd;
    std::mt19937 mt(rnd());
    return create_int_rand(mt, 0, N);
}

